public class Prog4 {
    static int num1 = 435; //Static variable
    public static void main(String[] args) {
        System.out.println(num1); // Prints the value of Static num1
        num1 = 45; // Changes the value of Static num1
        System.out.println(num1); // Prints the value of Static num1 after updating
    }
}
